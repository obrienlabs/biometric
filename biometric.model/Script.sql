CREATE TABLE gps_record (
	IDENT_ID BIGINT NOT NULL,
	ACCELX VARCHAR(255),
	ACCELY VARCHAR(255),
	ACCELZ VARCHAR(255),
	ACCURACY DECIMAL(10 , 10),
	ALTITUDE DECIMAL(10 , 10),
	bearing INT,
	geohash VARCHAR(255),
	GRAVX VARCHAR(255),
	GRAVY VARCHAR(255),
	GRAVZ VARCHAR(255),
	GYROX VARCHAR(255),
	GYROY VARCHAR(255),
	GYROZ VARCHAR(255),
	HEART1 INT,
	HEART2 INT,
	HRDEV1 VARCHAR(255),
	HRDEV2 VARCHAR(255),
	humidity VARCHAR(255),
	LATITUDE DECIMAL(10 , 10),
	light VARCHAR(255),
	LINACCX VARCHAR(255),
	LINACCY VARCHAR(255),
	LINACCZ VARCHAR(255),
	LONGITUDE DECIMAL(10 , 10),
	PRES DOUBLE,
	provider VARCHAR(255),
	PROX VARCHAR(255),
	RECV_SEQ BIGINT,
	ROTVECX VARCHAR(255),
	ROTVECY VARCHAR(255),
	ROTVECZ VARCHAR(255),
	SEND_SEQ BIGINT,
	speed DOUBLE,
	temp DOUBLE,
	teslaX VARCHAR(255),
	teslaY VARCHAR(255),
	teslaZ VARCHAR(255),
	tsStart BIGINT,
	tsStop BIGINT,
	userId BIGINT,
	version BIGINT,
	PRIMARY KEY (IDENT_ID)
) ENGINE=InnoDB;

